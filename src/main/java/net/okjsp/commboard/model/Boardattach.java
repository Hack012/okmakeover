package net.okjsp.commboard.model;

public class BoardattachVO {

	// 첨부ID
	private String attchId;
	
	// 첨부순서
	private String attchOrder;
	
	// 파일명
	private String fileName;
	
	// 저장파일명
	private String saveFilename;
	
	// 파일타입
	private String fileType;
	
	// 파일용량
	private String fileSize;
	
	// 그림높이
	private String imgHeight;
	
	// 그림넓이
	private String imgWidth;
	
	// 등록일시
	private String writeDate;
	
	// 변경일시
	private String updateDate;

	public String getAttchId() {
		return attchId;
	}

	public void setAttchId(String attchId) {
		this.attchId = attchId;
	}

	public String getAttchOrder() {
		return attchOrder;
	}

	public void setAttchOrder(String attchOrder) {
		this.attchOrder = attchOrder;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getSaveFilename() {
		return saveFilename;
	}

	public void setSaveFilename(String saveFilename) {
		this.saveFilename = saveFilename;
	}

	public String getFileType() {
		return fileType;
	}

	public void setFileType(String fileType) {
		this.fileType = fileType;
	}

	public String getFileSize() {
		return fileSize;
	}

	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}

	public String getImgHeight() {
		return imgHeight;
	}

	public void setImgHeight(String imgHeight) {
		this.imgHeight = imgHeight;
	}

	public String getImgWidth() {
		return imgWidth;
	}

	public void setImgWidth(String imgWidth) {
		this.imgWidth = imgWidth;
	}

	public String getWriteDate() {
		return writeDate;
	}

	public void setWriteDate(String writeDate) {
		this.writeDate = writeDate;
	}

	public String getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	
	
	
}
