package net.okjsp.community.model;

public class Clippingmapping {
	
	// 게시판ID
	private String boardId;
	
	// 글번호
	private String writeNo;
	
	// 스크랩ID
	private String clippingId;

	public String getBoardId() {
		return boardId;
	}

	public void setBoardId(String boardId) {
		this.boardId = boardId;
	}

	public String getWriteNo() {
		return writeNo;
	}

	public void setWriteNo(String writeNo) {
		this.writeNo = writeNo;
	}

	public String getClippingId() {
		return clippingId;
	}

	public void setClippingId(String clippingId) {
		this.clippingId = clippingId;
	}
	
 
	
}
